// while loop in javascript
let i = 1;
while (i<=10) {
    console.log(i)
    i++
}

let myArray = ["flash", "Batman", "Superman"]

let arr = 0
while (arr<myArray.length) {
    console.log(myArray[arr])
    arr++
}

// do while loop in javascript

let score = 1
do {
    console.log(score)
    score++
} while (score<=10);
